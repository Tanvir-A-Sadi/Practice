package myCodes;

public class EscapeSequence {

/*
 * Useful escape sequences
 *  \t - tab
 *  \n - new line
 *  the backslash is known as escape sequence and it tells the system to ignore whatever the following
 *  character is. For example - 
 *  \" will print the double quote
 *  \\ will print the backslash 
 *  
 */
	public static void main(String[] args) {
		System.out.println("My name is \"Tanvir\"");
		System.out.println("This is a double quote: \" ");
		System.out.println("This is a backslash \\");
	}

}
